    private void jSlider_TemperaturaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider_TemperaturaStateChanged
        // TODO add your handling code here:
         jSlider_Temperatura.setMaximum(255);
        if(this.Original!=null){
            jSlider_Temperatura.getValue();
            String v = ""+jSlider_Temperatura.getValue();
            this.jLabel_ValorTemperatura.setText(v);
            modificada = FiltrosEspaciales.modificarTemperatura(Original,jSlider_Temperatura.getValue());
            this.Modificada = modificada;
            this.jLabelImagenOriginal.setIcon(new ImageIcon(modificada.getScaledInstance(this.jLabelImagenOriginal.getWidth(),this.jLabelImagenOriginal.getHeight(),Image.SCALE_DEFAULT)));
        }
    }//GEN-LAST:event_jSlider_TemperaturaStateChanged

